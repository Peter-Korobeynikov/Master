function fn_autoinstall_extradata_menu($current_theme, $themes)
{
if (in_array($current_theme, $themes)) {
$m = db_get_field('SELECT menu_id FROM ?:menus_descriptions WHERE name like \'AB: Fast navigation (%\' LIMIT 1');
$m && db_query('UPDATE ?:bm_blocks_content SET content = ?s WHERE block_id IN (?n)', serialize(['menu' => $m]), db_get_fields('SELECT block_id FROM ?:bm_blocks WHERE properties LIKE \'%ab__fn_%\''));
}
}
